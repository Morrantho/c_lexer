#ifndef __TYPEDEF_H__
#define __TYPEDEF_H__

typedef char i8, *i8_p;
typedef short i16, *i16_p;
typedef int i32, *i32_p;
typedef long long int i64, *i64_p;

typedef unsigned char u8, *u8_p;
typedef unsigned short u16, *u16_p;
typedef unsigned int u32, *u32_p;
typedef unsigned long long int u64, *u64_p;

typedef float f32, *f32_p;
typedef double f64, *f64_p;

typedef char* char_p;
typedef char** char_pp;
typedef void* void_p;
typedef void** void_pp;

#endif/*__TYPEDEF_H__*/
